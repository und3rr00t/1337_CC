#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	s;
	int	o;

	i = 0;
	s = 1;
	o = 0;

	while ((nptr[i] == 32) || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			s *= -1;
		i++;
	}
	while (ft_isdigit(nptr[i]))
	{
		o = o * 10 + (nptr[i] - 48);
		i++;
	}
	return (o * s);
}
